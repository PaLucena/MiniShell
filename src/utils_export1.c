#include "../include/env.h"

int	check_spaces(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0' || str[i] != '=')
	{
		if (str[i] == 32 && (str[i + 1] == '=' || str[i - 1] == '='))
			return (1);
		i++;
	}
	return (0);
}
